/**
 * Orbital Platform — k6 Load Test
 *
 * Simulates realistic production traffic across all API endpoints.
 * Runs three phases: ramp-up → sustained load → ramp-down.
 *
 * SLO thresholds enforced (test fails if these are breached):
 *   - p95 latency < 500ms
 *   - p99 latency < 1500ms
 *   - Error rate  < 1%
 *   - /health/    always responds in < 200ms
 *
 * Usage (local):
 *   k6 run load-testing/k6/scripts/api-load-test.js \
 *     -e BASE_URL=http://localhost:8000
 *
 * Usage (CI — staging):
 *   k6 run load-testing/k6/scripts/api-load-test.js \
 *     -e BASE_URL=https://staging.your-domain.com \
 *     -e K6_CLOUD_TOKEN=$K6_CLOUD_TOKEN \
 *     --out cloud
 *
 * Usage (CI — production smoke):
 *   k6 run load-testing/k6/scripts/api-load-test.js \
 *     -e BASE_URL=https://api.your-domain.com \
 *     -e SCENARIO=smoke
 */

import http    from "k6/http";
import { check, group, sleep } from "k6";
import { Counter, Rate, Trend } from "k6/metrics";

// ── Custom metrics ────────────────────────────────────────────
const errorRate          = new Rate("error_rate");
const articleCreateCount = new Counter("articles_created");
const dbLatency          = new Trend("db_query_latency_proxy");

// ── Configuration ─────────────────────────────────────────────
const BASE_URL = __ENV.BASE_URL || "http://localhost:8000";
const SCENARIO = __ENV.SCENARIO || "load";

const SCENARIOS = {
  smoke: {
    executor:        "constant-vus",
    vus:             2,
    duration:        "1m",
    gracefulStop:    "10s",
  },
  load: {
    executor:        "ramping-vus",
    startVUs:        0,
    stages: [
      { duration: "2m",  target: 20  },   // ramp to 20 users over 2 min
      { duration: "5m",  target: 20  },   // hold for 5 min
      { duration: "2m",  target: 50  },   // spike to 50 users
      { duration: "3m",  target: 50  },   // hold spike
      { duration: "2m",  target: 0   },   // ramp down
    ],
    gracefulRampDown: "30s",
  },
  stress: {
    executor:        "ramping-vus",
    startVUs:        0,
    stages: [
      { duration: "5m",  target: 100 },
      { duration: "10m", target: 100 },
      { duration: "5m",  target: 200 },
      { duration: "10m", target: 200 },
      { duration: "5m",  target: 0   },
    ],
    gracefulRampDown: "60s",
  },
  soak: {
    executor:        "constant-vus",
    vus:             10,
    duration:        "1h",         // 1-hour soak test — finds memory leaks
    gracefulStop:    "60s",
  },
};

export const options = {
  scenarios: {
    [SCENARIO]: SCENARIOS[SCENARIO],
  },

  // ── SLO thresholds — test FAILS if any breach ──────────────
  thresholds: {
    // Overall latency SLO
    "http_req_duration":             ["p(95)<500", "p(99)<1500"],

    // Health check must always be fast
    "http_req_duration{name:health}": ["p(99)<200"],

    // API endpoints
    "http_req_duration{name:list}":   ["p(95)<300"],
    "http_req_duration{name:detail}": ["p(95)<300"],
    "http_req_duration{name:create}": ["p(95)<500"],

    // Error rate SLO: < 1%
    "error_rate": ["rate<0.01"],

    // All HTTP requests must succeed
    "http_req_failed": ["rate<0.01"],
  },
};

// ── Helpers ───────────────────────────────────────────────────
const headers = {
  "Content-Type": "application/json",
  "Accept":       "application/json",
};

function assertOk(res, name) {
  const ok = check(res, {
    [`${name}: status 2xx`]: (r) => r.status >= 200 && r.status < 300,
    [`${name}: body not empty`]: (r) => r.body && r.body.length > 0,
  });
  errorRate.add(!ok);
  return ok;
}

// ── Test scenarios ────────────────────────────────────────────
export default function () {
  // 40% of traffic: read-heavy users browsing articles
  group("Read flow", () => {
    const health = http.get(`${BASE_URL}/health/`, {
      tags: { name: "health" },
    });
    assertOk(health, "health check");

    const list = http.get(`${BASE_URL}/api/articles/`, {
      headers,
      tags: { name: "list" },
    });
    if (assertOk(list, "list articles")) {
      const data = JSON.parse(list.body);
      const articles = data.articles || [];

      // Follow-up: read a random individual article
      if (articles.length > 0) {
        const article = articles[Math.floor(Math.random() * articles.length)];
        const detail = http.get(`${BASE_URL}/api/articles/${article.id}/`, {
          headers,
          tags: { name: "detail" },
        });
        assertOk(detail, "article detail");
      }
    }

    sleep(Math.random() * 2 + 1);  // 1–3s think time
  });

  // 30% of traffic: writers creating and publishing
  if (Math.random() < 0.3) {
    group("Write flow", () => {
      const payload = JSON.stringify({
        title: `Load test article ${Date.now()}`,
        body:  "Generated by k6 load test to validate write throughput.",
      });

      const create = http.post(`${BASE_URL}/api/articles/`, payload, {
        headers,
        tags: { name: "create" },
      });

      if (assertOk(create, "create article")) {
        articleCreateCount.add(1);
        const created = JSON.parse(create.body);

        // Immediately publish the article
        const publish = http.patch(
          `${BASE_URL}/api/articles/${created.id}/`,
          null,
          { headers, tags: { name: "publish" } }
        );
        assertOk(publish, "publish article");
      }

      sleep(Math.random() * 3 + 2);  // 2–5s think time for writes
    });
  }
}

// ── Setup: seed some articles so reads have data ──────────────
export function setup() {
  console.log(`Running ${SCENARIO} scenario against ${BASE_URL}`);

  const health = http.get(`${BASE_URL}/health/`);
  if (health.status !== 200) {
    throw new Error(`Health check failed: ${health.status}. Is the server running?`);
  }

  // Create 5 seed articles for the read tests
  for (let i = 0; i < 5; i++) {
    const res = http.post(
      `${BASE_URL}/api/articles/`,
      JSON.stringify({ title: `Seed article ${i + 1}`, body: "Seed data for load test." }),
      { headers }
    );
    if (res.status === 201) {
      const article = JSON.parse(res.body);
      http.patch(`${BASE_URL}/api/articles/${article.id}/`, null, { headers });
    }
  }

  console.log("Setup complete — 5 seed articles created and published.");
}

// ── Teardown: summary ─────────────────────────────────────────
export function teardown(data) {
  console.log("Load test complete.");
  console.log(`Articles created during test: ${articleCreateCount}`);
}
